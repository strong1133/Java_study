package com.javastudy.dofirst.cpt03;

public class OperationEx5 {
    public static void main(String[] args) {

        /*  & (AND)
        int num1 = 5;
        int num2 = 10;
        int res = num1 & num2;
        System.out.println(res); */

        /* ^ (XOR)
        int num1 = 5;
        int num2 = 10;
        int res = num1 ^ num2;
        System.out.println(res); */

        /* ~ (반전)
        int num = 10;
        int res = ~num;
        System.out.println(res); */

        /* << 연산자
        int num = 5;
        int res = num << 2;
        System.out.println(res); */

        /* >> 연산자
        int num = 10;
        int res = num >> 2;
        System.out.println(res); */

    }
}
